package java2022BasicHomeworks;

public class DoWhileLoop {

	public static void main(String[] args) {
		int i = 1;
		do {
			System.out.println(i);
			i += 1;
		} while (i < 10);

		System.out.println("The do-while loop finished");

		int j = 50;
		do {
			System.out.println(j);
			j += 1;
		} while (j < 10);

		System.out.println("The do-while loop finished");

	}

}
